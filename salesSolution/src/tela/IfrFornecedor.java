    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        String nomeFornecedor = tfdDescricaoFornecedor1.getText();
        String emailFornecedor = tfdEmailFornecedor.getText();
        String telefoneFornecedor = tffTelefone.getText();
        String cnpjFornecedor = tffCNPJ.getText();

        // cricao obj Cidade
        Fornecedor fornecedor = new Fornecedor();
        fornecedor.setNome(nomeFornecedor);
        fornecedor.setEmail(emailFornecedor);
        fornecedor.setTelefone(telefoneFornecedor);
        fornecedor.setCnpj(cnpjFornecedor);

        // criacao do obj CidadeDAO
        FornecedorDAO fornecedorDAO = new FornecedorDAO();

       if (idFornecedor == 0) { // representa uma INSERCAO
            if (fornecedorDAO.salvar(fornecedor) == null) {
                tfdDescricaoFornecedor1.setText("");

                JOptionPane.showMessageDialog(this, "Registro salvo com sucesso!");

                tfdDescricaoFornecedor1.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "Problemas ao salvar registro!");
            }
        } else {
            if (fornecedorDAO.atualizar(fornecedor) == null) {
                tfdDescricaoFornecedor1.setText("");

                JOptionPane.showMessageDialog(this, "Registro salvo com sucesso!");

                tfdDescricaoFornecedor1.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "Problemas ao salvar registro!");
            }
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

