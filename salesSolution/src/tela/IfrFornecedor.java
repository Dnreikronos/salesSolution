    private void btnExcluir2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluir2ActionPerformed
        String idTabela = String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 0));

        idFornecedor = Integer.parseInt(idTabela);

        if (new FornecedorDAO().excluir(idFornecedor) == null) {
            JOptionPane.showMessageDialog(this, "Registro excluído com sucesso!");

            new FornecedorDAO().popularTabela(jTable1, "");
        } else {
            JOptionPane.showMessageDialog(this, "Problemas ao excluir registro!");
        }

        idFornecedor = 0;
    }//GEN-LAST:event_btnExcluir2ActionPerformed

    private void btnEditar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditar2ActionPerformed
        String idTabela = String.valueOf(jTable1.getValueAt(jTable1.getSelectedRow(), 0));

        idFornecedor = Integer.parseInt(idTabela);

        Fornecedor fornecedor = new FornecedorDAO().consultarId(idFornecedor);

        if (fornecedor != null) {
            jTabbedPane1.setSelectedIndex(1);

            tfdDescricaoFornecedor1.setText(fornecedor.getNome());
            tfdDescricaoFornecedor1.setText(fornecedor.getEmail());
            tfdDescricaoFornecedor1.setText(fornecedor.getTelefone());
            tfdDescricaoFornecedor1.setText(fornecedor.getCnpj());
            tfdDescricaoFornecedor1.requestFocus();
        } else {
            JOptionPane.showMessageDialog(this, "Id do fornecedor não encontrado!");
        }
    }//GEN-LAST:event_btnEditar2ActionPerformed

    
    
