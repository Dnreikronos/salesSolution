    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (tfd_cc_email.getText().trim().length() > 0 && String.valueOf(tfd_cc_senha.getPassword()).trim().length() > 0){
            Usuario u  = new Usuario();
            u.setEmail(tfd_cc_email.getText());
            u.setSenha(String.valueOf(tfd_cc_senha.getPassword()));
            
            if(new usuarioDAO().Autenticar(u)){
                new FrmPrincipal().setVisible(true);
                this.dispose();
            }
            else {
                JOptionPane.showMessageDialog(this, "Usu√°rio ou senha incorretos!");
            }
        }
        else {
                JOptionPane.showMessageDialog(this, "Preencha todos so campos antes de acessar!");
                tfd_cc_email.requestFocus();
            }
    }//GEN-LAST:event_jButton1ActionPerformed

